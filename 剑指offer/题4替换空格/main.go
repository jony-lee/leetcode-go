// 实现一个函数将字符串中的每个函数替换成"%20"

// 思路
// 首先计算字符串的空格数,计算替换后的字符长度,对原字符进行扩容
// 使用两个指针p1,p2,p1指向原字符串末尾,p2指向扩容后的字符串末尾
// 分别从后向前移动p2复制p1的字符,当碰到空格时,p2向前移动3格,复制%20
// 当p1,p2指向同一位置时结束

func replaceBlank(s *string) {
	if len(*s) == 0 {
		return
	}
	p1 := len(*s) - 1
	n := 0
	for _, i := range *s {
		if i == ' ' {
			n += 1
		}
	}
	p2 := len(*s) - 1 + n

}
